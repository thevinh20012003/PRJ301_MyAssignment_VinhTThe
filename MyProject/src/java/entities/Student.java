package entities;

import java.util.Date;

/**
 *
 * @author Admin
 */
public class Student {

    private int id;
    private String name;
    private Date dob;
    private int gender;// 1-nam, 2-nu, 3-other

    public Student(int id, String name, Date dob, int gender) {
        this.id = id;
        this.name = name;
        this.dob = dob;
        this.gender = gender;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Date getDob() {
        return dob;
    }

    public void setDob(Date dob) {
        this.dob = dob;
    }

    public int getGender() {
        return gender;
    }

    public void setGender(int gender) {
        this.gender = gender;
    }
}
